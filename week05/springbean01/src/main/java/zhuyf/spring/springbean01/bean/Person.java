package zhuyf.spring.springbean01.bean;

/**
 * 人员信息
 */
public class Person {

    /**
     * 人员ID
     */
    private String id;

    /**
     * 姓名
     */
    private String name;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
